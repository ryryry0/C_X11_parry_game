// ポイントサイズに合ったフォントのファイル名を与える関数
const char *get_fixed_font(int point_size)
{
    switch (point_size)
    {
    case 10:
        return "-misc-fixed-medium-r-normal--10-100-75-75-c-60-iso8859-1";
    case 13:
        return "-misc-fixed-medium-r-normal--13-120-75-75-c-70-iso8859-1";
    case 16:
        return "-sony-fixed-medium-r-normal--16-120-100-100-c-80-iso8859-1";
    case 20:
        return "-misc-fixed-medium-r-normal--20-200-75-75-c-100-iso8859-1";
    case 24:
        return "-sony-fixed-medium-r-normal--24-230-75-75-c-120-iso8859-1";
    case 32:
        return "-misc-fixed-medium-r-normal--32-320-75-75-c-160-iso8859-1";
    case 48:
        return "-misc-fixed-medium-r-normal--48-480-75-75-c-240-iso8859-1";
    default:
        fprintf(stderr, "警告: point_size=%d に対応するフォントがありません。fixed を使用します。\n", point_size);
        return "fixed"; // fallback
    }
}

// get_fixed_font() でフォント名を取得し、それを使って XLoadQueryFont() に渡してフォント情報を読み込む関数
void g_text_font(int x, int y, char *str, int point_size)
{
    const char *fontname = get_fixed_font(point_size);
    XFontStruct *font = XLoadQueryFont(disp, fontname);

    if (!font)
    {
        fprintf(stderr, "フォント %s が見つかりませんでした。デフォルトフォントを使用します。\n", fontname);
        font = XLoadQueryFont(disp, "fixed");
        if (!font)
        {
            fprintf(stderr, "デフォルトフォントも読み込めませんでした。\n");
            return;
        }
    }

    // 一時的なGCを作ってフォントを設定して描画
    GC temp_gc = XCreateGC(disp, win, 0, NULL);
    XSetFont(disp, temp_gc, font->fid);
    XSetForeground(disp, temp_gc, BlackPixel(disp, scrn)); // 色も合わせておく
    XSetBackground(disp, temp_gc, WhitePixel(disp, scrn));
    XDrawString(disp, get_draw_target(), temp_gc, x, y, str, strlen(str));

    // 解放
    XFreeGC(disp, temp_gc);
    XFreeFont(disp, font);
}

// 4つの頂点（四角形）を塗りつぶす関数。
void g_fill_quad(int x0, int y0, int x1, int y1,
                 int x2, int y2, int x3, int y3)
{
    XPoint points[4];
    points[0].x = x0;
    points[0].y = y0;
    points[1].x = x1;
    points[1].y = y1;
    points[2].x = x2;
    points[2].y = y2;
    points[3].x = x3;
    points[3].y = y3;

    // g_fill_polygonでは閉じなくてもOK
    // 凸多角形convexとして描画
    XFillPolygon(disp, get_draw_target(), gc, points, 4, Convex, CoordModeOrigin);
}

