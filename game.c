// 書くだけ
void draw_ximage(Display *disp, Drawable target, GC gc, XImage *img, int center_x, int center_y)
{
    if (!img || !disp)
        return;

    int x = center_x - img->width / 2;
    int y = center_y - img->height / 2;

    XPutImage(disp, target, gc, img,
              0, 0, // src_x, src_y
              x, y, // dst_x, dst_y
              img->width, img->height);
}

