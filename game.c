// 筆を持ち変える関数
void g_rgb(unsigned short r, unsigned short g, unsigned short b)
{
    XColor xc;
    xc.red = r;
    xc.green = g;
    xc.blue = b;
    XAllocColor(disp, DefaultColormap(disp, scrn), &xc);
    // 筆セットをいじり、色を変えてあげる
    XSetForeground(disp, gc, xc.pixel);
}

