// img_はドット数
// RGB形式の画像配列（image[y][x][RGB]）を、XImageに変換し、指定サイズにリサイズして返す関数
XImage *create_resized_ximage_from_array(Display *disp, Visual *vis, int depth,
                                         unsigned char image[][100][3], int img_width, int img_height,
                                         int target_width, int target_height)
{
    int bytes_per_pixel = 4;
    int stride = target_width * bytes_per_pixel;

    // error
    unsigned char *data = (unsigned char *)malloc(target_height * stride);
    if (!data)
    {
        fprintf(stderr, "メモリ確保失敗\n");
        return NULL;
    }

    // 最近傍補間で画像を拡大・縮小
    for (int y = 0; y < target_height; y++)
    {
        for (int x = 0; x < target_width; x++)
        {
            int src_x = x * img_width / target_width;
            int src_y = y * img_height / target_height;

            unsigned char r = image[src_y][src_x][0];
            unsigned char g = image[src_y][src_x][1];
            unsigned char b = image[src_y][src_x][2];

            int idx = (y * target_width + x) * 4;
            data[idx + 0] = b;
            data[idx + 1] = g;
            data[idx + 2] = r;
            data[idx + 3] = 0;
        }
    }

    // XCreateImage() を使って、上で生成した生データ data を XImage に変換。
    return XCreateImage(disp, vis, depth, ZPixmap, 0, (char *)data, target_width, target_height, 32, 0);
}

