// hitスレッド
void *hit_hit(void *arg)
{
    hit = 1;
    usleep(100000);
    e_hp -= 1;
    hit = 0;

    if (e_hp <= 0)
    {
        e_hp = 0;
        game_over = 0; // clear
        game_running = 0;
    }

    return NULL;
}

// hitスレッド
void *hit_hit_hit(void *arg)
{
    hit1 = 1;
    usleep(100000);
    if (d == 0 && e_hp % 5 == 0)
    {
        pthread_t t;
        pthread_create(&t, NULL, dd, NULL);
        pthread_detach(t);
    }
    hit1 = 0;

    return NULL;
}

void *dd(void *arg)
{
    d = 1;
    sleep(4);
    main_player.player_HP -= 10;
    if (main_player.player_HP <= 0)
    {
        main_player.player_HP = 0;
    }
    d = 0;

    return NULL;
}
