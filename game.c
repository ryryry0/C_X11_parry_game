// Xサーバーに接続し、ウインドウを作成、表示、初期化する
void initwin()
{
    XSetWindowAttributes wa; // 属性設定構造体
    XSizeHints hints;        // ウインドウサイズ用構造体
    if ((disp = XOpenDisplay(NULL)) == NULL)
    {
        FatalError("Can't open display."); // ディスプレイに接続失敗
    }
    scrn = DefaultScreen(disp); // ディスプレイ内の「デフォルトスクリーン番号」を取得（通常は0）

    // ウインドウを作成
    win = XCreateSimpleWindow(disp, RootWindow(disp, scrn), 0, 0, width, height, 2, 2, WhitePixel(disp, scrn));
    /*•	disp: ディスプレイ接続
     •	RootWindow(...): 親ウィンドウ
     •	0, 0: 表示位置（左上）
     •	width, height: サイズ
     •	2: 枠線の幅
     •	2: 枠線の色（黒）
     •	WhitePixel: 背景色*/

    // ウィンドウのサイズを固定に設定（リサイズ不可にしている）
    hints.flags = PSize | PMinSize | PMaxSize;
    hints.width = hints.min_width = hints.max_width = width;
    hints.height = hints.min_height = hints.max_height = height;
    XSetNormalHints(disp, win, &hints);

    // 描画設定（Graphics Context）を作成し、前景色（描画色）を黒、背景色を白に。
    gc = XCreateGC(disp, win, 0, 0); // 筆ペン情報の取得
    XSetForeground(disp, gc, BlackPixel(disp, scrn));
    XSetBackground(disp, gc, WhitePixel(disp, scrn));
    XSetFunction(disp, gc, GXcopy);

    // イベント（画面の再描画、キー入力、サイズ変更通知など）を受け取る設定
    XSelectInput(disp, win, ExposureMask | KeyPressMask | StructureNotifyMask); // ← MapNotify を含める

    // ウィンドウを画面に表示し（Map）、すぐに描画を反映
    XMapRaised(disp, win);
    XFlush(disp);

    // ダブルバッファ用Pixmapを作成
    // ウィンドウと同じサイズ・深さの「裏画面（バックバッファ）」を作成。これに描いて、あとで転送する。
    backbuffer = XCreatePixmap(disp, win, width, height, DefaultDepth(disp, scrn));

    // ウィンドウが表示されるまで待つ（MapNotify イベント）
    XEvent e;
    while (1)
    {
        XNextEvent(disp, &e);
        if (e.type == MapNotify)
            break; // 表示完了を待つ
    }
}

// ダブルバッファリングon,off
void g_set_buffering(int flag)
{
    use_backbuffer = flag;
}

// バックバッファが有効なら、その内容を XCopyArea() で win にコピー（画面に反映）
void g_flush()
{
    if (use_backbuffer)
    {
        // バッファ内容をウィンドウにコピーしてからフラッシュ
        XCopyArea(disp, backbuffer, win, gc, 0, 0, width, height, 0, 0);
    }
    // 描画を即座に反映
    // if内が行われないと何も起こらない
    XFlush(disp); // ウインドウを表示する関数
}

// 描画先を変える関数
Drawable get_draw_target()
{
    return use_backbuffer ? backbuffer : win;
}

