            float z = x_max - balls[i].ball_z;
            if (z < 0)
                z = 0;
            if (z > 800)
                z = 800;

            float scale = (800.0f - z) / 800.0f;
            float px = 400.0f + (x - 400.0f) * scale;
            float py = 400.0f + (y - 400.0f) * scale;
            int size = 5 + (int)(60.0f * scale);

            // 明度調整（手前1.0、奥0.0）
            float intensity = pow(scale, 1.1);

            // 白に向かって補間（明るくなる）
            int r = base_r + (int)((65535 / 2 - base_r) * (1.0f - intensity));
            int g = base_g + (int)((65535 / 2 - base_g) * (1.0f - intensity));
            int b = base_b + (int)((65535 / 2 - base_b) * (1.0f - intensity));

            // mpmode予測線
            if (main_player.mp_mode == 1)
            {
                g_rgb(60000, 0, 60000);
                g_line(400, 400, x, y);
                g_fill(x - 5, y - 5, 10, 10);
            }

            // 色を設定
            g_rgb(r, g, b);

            g_fill_arc((int)(px - size / 2), (int)(py - size / 2), size, size, 0, 360 * 64);

            // かげ
            float s_px = 400.0f + (x - 400.0f) * scale;
            float s_py = 400.0f + (800.0f - 400.0f) * scale;
            g_rgb(0, 18000, 28000);
            g_fill_arc((int)(s_px - size / 2), (int)(s_py - (size / 2) / 2), size, size / 2, 0, 360 * 64);

